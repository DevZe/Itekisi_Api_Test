package itekisi_models;

public class Taxi {
    public String Id;
    public String From;
    public String To;
    public String Price;
    public String Location;

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getFrom() {
        return From;
    }

    public void setFrom(String from) {
        From = from;
    }

    public String getTo() {
        return To;
    }

    public void setTo(String to) {
        To = to;
    }

    public String getPrice() {
        return Price;
    }

}

